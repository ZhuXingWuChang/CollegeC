/*
数据的拆位与重组
这里我们以这个题目为例： 
输入一个三位数，把它的个位放到百位，百位放到十位，十位放到个位。 
*/
#include<stdio.h>
void main(void)
{
	int num,g,s,b;
	printf("输入一个三位数:");
	scanf("%d",&num);
	
	b=num/100;	//123=1*100+23 , 123/100=1
	s=num/10%10;//123=12*10+3  , 123/10=12 , 12=1*10+2 , 12%10=2
	g=num%10;	//123=12*10+3  , 123%10=3
	
	num=g*100 + b*10 + s;
	printf("重组后的数:%d",num);
}
